function buildQuiz(){
  var box = byId("quiz"); if(!box) return;
  var i, html="";
  for(i=0;i<QUIZ.length;i++){
    var it = QUIZ[i], j;
    html += '<div class="question">'+(i+1)+') '+it.q+'</div>';
    for(j=0;j<it.opts.length;j++){
      html += '<label class="option"><input type="radio" name="q'+i+'" value="'+j+'"> '+it.opts[j]+'</label>';
    }
  }
  box.innerHTML = html;

  var btnCal = byId("btnCalificar"), btnRe = byId("btnReiniciar");
  if(btnCal) btnCal.onclick = function(){
    var correct=0,total=QUIZ.length, i, sel;
    for(i=0;i<total;i++){
      sel = document.querySelector('input[name="q'+i+'"]:checked');
      if(sel && parseInt(sel.value,10)===QUIZ[i].ans) correct++;
    }
    var wrong = total - correct;
    var pct = Math.round(correct/total*100);
    var passed = pct >= 70;

    // Estrellas 1–5 según % (≥90:5; ≥70:4; ≥50:3; ≥30:2; <30:1)
    var stars = (pct>=90?5:pct>=70?4:pct>=50?3:pct>=30?2:1);
    var starsHtml = '<div style="font-size:28px;letter-spacing:2px;margin-bottom:6px;">'
                  + '★'.repeat(stars) + '☆'.repeat(5-stars) + '</div>';

    var title = passed ? '¡Excelente! Pasaste el quiz' : 'Necesitas repasar';
    var msg   = 'Aciertos: <b>'+correct+'</b> &nbsp;·&nbsp; Errores: <b>'+wrong+'</b> &nbsp;·&nbsp; Puntaje: <b>'+pct+'%</b>';

    ensureSweetAlert(function(){
      if(window.Swal){
        Swal.fire({
          icon: passed ? 'success' : 'info',
          title: title,
          html: starsHtml + '<div style="font-size:16px">'+msg+'</div>',
          showCancelButton: true,
          confirmButtonText: passed ? 'Repetir' : 'Reintentar',
          cancelButtonText: 'Cerrar',
          allowOutsideClick: true
        }).then(function(res){
          if(res.isConfirmed){ render("quiz"); }
        });
      }else{
        // Fallback simple si no cargó la librería
        alert(title+'\n'+msg);
        if(!passed) render("quiz");
      }
    });

    // (opcional) también reflejar algo en #quizResult si quieres
    var res = byId("quizResult");
    if(res) res.innerHTML = '<b>Resultado:</b> '+correct+' / '+total+' ('+pct+'%).';
  };

  if(btnRe) btnRe.onclick = function(){ render("quiz"); };
}

