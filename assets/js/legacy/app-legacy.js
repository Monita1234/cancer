async function initTesis(){
  const grid = document.getElementById('tesisGrid');
  if(!grid) return;

  // Ruta base dinámica (respeta <base> y subcarpetas)
  const BASE_AUTO = new URL('./assets/tesis/', document.baseURI).href;
  const BASES = [ BASE_AUTO, './assets/tesis/', 'assets/tesis/', '/assets/tesis/' ];
  const encSeg = s => s.split('/').map(encodeURIComponent).join('/');

  function variants(file){
    const set = new Set(), out = [];
    const add = s => { if(!set.has(s)){ set.add(s); out.push(s); } };
    const raw = String(file);

    const norms = [
      raw,
      raw.trim(),
      raw.replace(/\u00A0/g,' '),                         // NBSP -> espacio normal
      raw.normalize('NFC'),
      raw.normalize('NFD'),
      raw.normalize('NFD').replace(/\p{Diacritic}/gu,''), // sin tildes
    ].map(s => s
      .replace(/[–—]/g,'-')
      .replace(/[’‘]/g,"'")
      .replace(/[“”„]/g,'"')
      .replace(/\s+/g,' ')
      .replace(/\.\.+/g,'.')
    );

    norms.forEach(add);
    norms.forEach(s => add(s.toLowerCase()));
    norms.forEach(s => add(s.replace(/\.pdf$/i,'.pdf')));
    return out;
  }

  async function resolveUrl(file){
    const attempts = [];
    for(const base of BASES){
      for(const v of variants(file)){
        for(const url of [ base + encSeg(v), base + encodeURI(v), base + v ]){
          try{
            const g = await fetch(url, { cache:'no-store' });
            attempts.push([g.status, url]);
            if(g.ok) return { url, attempts };
          }catch(e){
            attempts.push(['ERR', url]);
          }
        }
      }
    }
    return { url:null, attempts };
  }
  

  function card(file, title, res){
    const ok = !!res?.url;
    const t = title || file;
    const info = '';
    const btns = ok
      ? `<a class="btn primary" href="${res.url}" target="_blank" rel="noopener">Abrir</a>
         <a class="btn" href="${res.url}" download>Descargar</a>`
      : `<span class="btn" style="opacity:.6;pointer-events:none">Abrir</span>
         <span class="btn" style="opacity:.6;pointer-events:none">Descargar</span>`;

    return `
      <div class="card equal" style="min-width:260px">
        <div class="pdf-thumb"><img src="${PDF_ICON}" alt="PDF"></div>
        <h4 style="margin:6px 0 4px">${t}</h4>
        <div class="btn-row">${btns}</div>
        <small style="${ok?'opacity:.7':'color:#b91c1c'}">${ok?file:'Revisa mayúsculas/tildes/corchetes o renómbralo'}</small>
        <div style="margin-top:4px;font-size:12px;${ok?'opacity:.7':'color:#b91c1c'}">${info}</div>
      </div>`;
  }

  // Aviso si es file://
  if(location.protocol === 'file:'){
    grid.innerHTML = `
      <div class="card full" style="color:#b45309">
        Estás abriendo el sitio como <b>file://</b>. Para que <code>fetch</code> funcione con PDFs, abre con un servidor local
        (p. ej. VSCode Live Server) o súbelo a tu hosting.
      </div>`;
  }

  // Carga manifest (JSON o JS)
  let items = [];
  try{
    const res = await fetch('./assets/tesis/manifest.json', {cache:'no-store'});
    items = await res.json();
  }catch(_){
    try{
      await new Promise((ok,bad)=>{
        const s=document.createElement('script');
        s.src='./assets/tesis/manifest.js?ts='+Date.now();
        s.onload=ok; s.onerror=bad; document.head.appendChild(s);
      });
      items = Array.isArray(window.TESIS_MANIFEST)?window.TESIS_MANIFEST:[];
    }catch(_){}
  }
  items = (Array.isArray(items)?items:[]).filter(x => x && /\.pdf$/i.test(x.file||''));

  if(!items.length){ grid.innerHTML = '<div class="card full">No hay PDFs listados.</div>'; return; }

  grid.innerHTML = '<div class="card full">Comprobando archivos…</div>';
  const outs = [];
  for(const it of items){
    const res = await resolveUrl(String(it.file||''));
    outs.push( card(it.file, it.title, res) );
  }
  grid.innerHTML = outs.join('');
}



function renderTesisGrid(){
  var grid = byId('tesisGrid'); if(!grid) return;
  tesisList().then(function(items){
    if(!items.length){
      grid.innerHTML = '<div class="card"><p>No hay PDFs cargados.</p></div>';
      return;
    }
    items.sort(function(a,b){ return (b.lastModified||0)-(a.lastModified||0); });
    var html='';
   items.forEach(function(f){
  var kb  = Math.max(1, Math.round((f.size||0)/1024));
  var url = blobUrlFor(f.id, f.blob);

  html += '<div class="card">'
        +   '<div class="pdf-thumb"><img src="'+PDF_ICON+'" alt="Archivo PDF"></div>'   // ← AÑADIDO
        +   '<h3><a href="#" data-open="'+f.id+'">'+escapeHtml(f.name)+'</a></h3>'
        +   '<p>'+kb+' KB</p>'
        +   '<div class="btn-row">'
        +     '<a class="btn primary" data-open="'+f.id+'"><svg class="ico"><use xlink:href="#i-play"/></svg> Ver</a>'
        +     '<a class="btn" data-download="'+f.id+'"><svg class="ico"><use xlink:href="#i-download"/></svg> Descargar</a>'
        +     '<a class="btn" data-remove="'+f.id+'">Eliminar</a>'
        +   '</div>'
        + '</div>';
});

    grid.innerHTML = html;

    grid.querySelectorAll('[data-open]').forEach(function(a){
      a.onclick = function(e){ e.preventDefault(); var id=+this.getAttribute('data-open'); var it = items.find(x=>x.id===id); if(it){ window.open(blobUrlFor(it.id,it.blob),'_blank'); } };
    });
    grid.querySelectorAll('[data-download]').forEach(function(a){
      a.onclick = function(e){ e.preventDefault(); var id=+this.getAttribute('data-download'); var it = items.find(x=>x.id===id); if(!it) return;
        var link=document.createElement('a'); link.href=blobUrlFor(it.id,it.blob); link.download=it.name; document.body.appendChild(link); link.click(); setTimeout(function(){ link.remove(); }, 300);
      };
    });
    grid.querySelectorAll('[data-remove]').forEach(function(a){
      a.onclick = function(e){ e.preventDefault(); var id=+this.getAttribute('data-remove'); tesisRemove(id).then(renderTesisGrid); };
    });
  });
}


