/* ====== CAROUSEL (ES5, autoplay + flechas + dots) ====== */
function initCarousel(id, intervalMs){
  var el = byId(id); if(!el) return;
  var slides = el.querySelectorAll('.slide');
  var dotsBox = el.querySelector('.c-dots');
  var prev = el.querySelector('.c-prev');
  var next = el.querySelector('.c-next');
  var i, idx = 0, timer, len = slides.length;

  function setActive(n){
    idx = (n + len) % len;
    for(i=0;i<len;i++){
      slides[i].className = slides[i].className.replace(' active','');
    }
    slides[idx].className += ' active';

    if(dotsBox){
      var dots = dotsBox.children;
      for(i=0;i<dots.length;i++){
        dots[i].className = dots[i].className.replace(' active','');
      }
      if(dots[idx]) dots[idx].className += ' active';
    }
  }
  function play(){ stop(); timer = setInterval(function(){ setActive(idx+1); }, intervalMs||4000); }
  function stop(){ if(timer) clearInterval(timer); }

  // dots
  if(dotsBox && dotsBox.children.length===0){
    for(i=0;i<len;i++){
      var d=document.createElement('div');
      d.className='c-dot'+(i===0?' active':'');
      (function(n){ d.onclick=function(){ setActive(n); }; })(i);
      dotsBox.appendChild(d);
    }
  }
  // controls
  if(prev) prev.onclick=function(){ setActive(idx-1); };
  if(next) next.onclick=function(){ setActive(idx+1); };

  el.addEventListener('mouseenter', stop);
  el.addEventListener('mouseleave', play);

  setActive(0); play();
}

