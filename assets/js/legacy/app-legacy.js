// ====== TESIS (persistente con IndexedDB) ======
var TDB = { db:null, urls:{} };

function tesisDB(){
  return new Promise(function(resolve,reject){
    if(TDB.db) return resolve(TDB.db);
    var req = indexedDB.open('tesisDB',1);
    req.onupgradeneeded = function(e){
      var db = e.target.result;
      var store = db.createObjectStore('pdfs', { keyPath:'id', autoIncrement: true });
      store.createIndex('by_date','lastModified');
    };
    req.onsuccess = function(e){ TDB.db = e.target.result; resolve(TDB.db); };
    req.onerror = function(e){ reject(e.target.error); };
  });
}

function tesisList(){ return tesisDB().then(function(db){
  return new Promise(function(resolve,reject){
    var tx = db.transaction('pdfs','readonly');
    var store = tx.objectStore('pdfs');
    var req = store.getAll();
    req.onsuccess = function(){ resolve(req.result||[]); };
    req.onerror = function(e){ reject(e.target.error); };
  });
});}

function tesisAddFiles(fileList){
  return tesisDB().then(function(db){
    return Promise.all(Array.from(fileList).map(function(f){
      if(!f || f.type!=='application/pdf') return null;
      return new Promise(function(resolve,reject){
        var tx = db.transaction('pdfs','readwrite');
        var store = tx.objectStore('pdfs');
        var rec = { name:f.name, size:f.size, lastModified:f.lastModified||Date.now(), blob:f };
        var req = store.add(rec);
        req.onsuccess = function(){ resolve(req.result); };
        req.onerror = function(e){ reject(e.target.error); };
      });
    }));
  });
}

function tesisRemove(id){
  return tesisDB().then(function(db){
    return new Promise(function(resolve,reject){
      var tx = db.transaction('pdfs','readwrite');
      var store = tx.objectStore('pdfs');
      var req = store.delete(id);
      req.onsuccess = function(){
        if(TDB.urls[id]){ try{ URL.revokeObjectURL(TDB.urls[id]); }catch(_){} delete TDB.urls[id]; }
        resolve(true);
      };
      req.onerror = function(e){ reject(e.target.error); };
    });
  });
}

function blobUrlFor(id, blob){
  if(TDB.urls[id]) return TDB.urls[id];
  var u = URL.createObjectURL(blob);
  TDB.urls[id] = u;
  return u;
}

